#include <bits/stdc++.h>
using namespace std;
int main() 
{
    long long int x,y;
    cin >> x >> y;
    // Printing the sum of x and y
    cout << x << " + " << y << " = " << x + y << endl;
    // Printing the multiplication of x and y
    cout << x << " * " << y << " = " << x * y << endl;
    // Printing the subtraction of x and y
    cout << x << " - " << y << " = " << x - y << endl;
    return 0;
}
